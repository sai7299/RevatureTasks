#List functions

#1-->To check number of elements in the list.
list1 = [1, 2, 3]
print(len(list1))

#2-->To add an item to the end of the list.
list1 = [1, 2, 3]
list1.append(4)
print(list1)

#3-->To insert an item at a specified index.
list2 = [1, 3, 4]
list2.insert(1, 2)
print(list2)  # Output: [1, 2, 3, 4]

#4-->To remove an item.
list2 = [1, 3, 4]
list2.remove(3)
print(list2)

#5-->To reverse the element.

list2=[1,2,3,4,5]
list2.reverse()
print(list2)



